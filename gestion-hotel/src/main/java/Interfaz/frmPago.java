    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (txtimpuestos.getText().length() == 0) {
            JOptionPane.showConfirmDialog(getParent(), "Debe ingresar el impuesto correspondiente");
            txtimpuestos.requestFocus();
            return;
        }
        if (txtnum_comprobante.getText().length() == 0) {
            JOptionPane.showConfirmDialog(getParent(), "Debe ingresar el nÃºmero de comprobante de pago");
            txtnum_comprobante.requestFocus();
            return;
        }
        if (dcfecha_emision.getDate() == null) {
            JOptionPane.showConfirmDialog(getParent(), "Debe ingresar la fecha de emision del comprobante");
            dcfecha_emision.requestFocus();
            return;
        }
        if (dcfecha_pago.getDate() == null) {
            JOptionPane.showConfirmDialog(getParent(), "Debe ingresar la fecha del pago");
            dcfecha_pago.requestFocus();
            return;
        }

        vpago dts = new vpago();
        fpago func = new fpago();

        dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
        int seleccionado = cbotipo_comprobante.getSelectedIndex();
        dts.setTipo_comprobante(cbotipo_comprobante.getItemAt(seleccionado));
        dts.setNum_comprobante(txtnum_comprobante.getText());
        dts.setImpuestos(Double.valueOf(txtimpuestos.getText()));
        dts.setTotal_pago(Double.valueOf(txttotal_pago.getText()));
        
        Calendar cal;
        cal=dcfecha_emision.getCalendar();
        dts.setFecha_emision(this.fecha_dc(cal));
        cal=dcfecha_pago.getCalendar();
        dts.setFecha_pago(this.fecha_dc(cal));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(getParent(), "El pago por $"+
                        txttotal_pago.getText()+" del cliente " +txtcliente.getText()+
                        " ha sido realizado con exito");
                mostrar(idreserva);
                inhabilitar();
                
                //desocupar la habitacion
                fhabitacion func2 = new fhabitacion();
                vhabitacion dts2 = new vhabitacion();
                
                dts2.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func2.desocupar(dts2);
                
                //cancelar la reserva
                freserva func3 = new freserva();
                vreserva dts3 = new vreserva();
                
                dts3.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                func3.pagar(dts3);
                
                        
            }
        } else if (accion.equals("editar")) {
            dts.setIdpago(Integer.parseInt(txtidpago.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(getParent(), "El pago del cliente " +txtcliente.getText()+
                        " se ha modificado satisfactoriamente");
                mostrar(idreserva);
                inhabilitar();
            }

        }
    }//GEN-LAST:event_btnguardarActionPerformed

